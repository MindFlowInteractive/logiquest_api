import { IsInt, Min, IsOptional, Max } from 'class-validator';
import { Type } from 'class-transformer';

export class CategoryPaginationDto {
  @IsOptional()
  @Type(() => Number)
  @IsInt()
  @Min(1)
  page?: number = 1;

  @IsOptional()
  @Type(() => Number)
  @IsInt()
  @Min(1)
  @Max(100)
  limit?: number = 20;
}
